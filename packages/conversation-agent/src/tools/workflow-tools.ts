// ---------------------------------------------------------------------------
// @kata-agent/conversation-agent — QA Workflow tools. The conversation layer
// owns only tool schemas and permission checks; a controller injected by the
// host application performs real WorkflowEngine/SkillRunner operations.
// ---------------------------------------------------------------------------
import type { ConversationTool, ToolResult, ToolContext } from "../types";

export interface WorkflowToolRun {
  runId: string;
  workflowName: string;
  status: string;
  currentNode?: string;
  project?: string;
  feature?: string;
  featureDir?: string;
}

export interface WorkflowStartInput {
  workflowName: string;
  project?: string;
  feature?: string;
  sourceUrl?: string;
  runId?: string;
  mode?: "mock" | "real";
  inputs?: Record<string, string>;
}

export interface WorkflowRunLookupInput {
  runId: string;
  project?: string;
  feature?: string;
  featureDir?: string;
  mode?: "mock" | "real";
}

export interface WorkflowFindRunsInput {
  project?: string;
  workflowName?: string;
}

export interface WorkflowToolController {
  start(input: WorkflowStartInput, context: ToolContext): Promise<WorkflowToolRun>;
  status(
    input: WorkflowRunLookupInput,
    context: ToolContext,
  ): Promise<WorkflowToolRun>;
  resume(
    input: WorkflowRunLookupInput,
    context: ToolContext,
  ): Promise<WorkflowToolRun>;
  findRuns(
    input: WorkflowFindRunsInput,
    context: ToolContext,
  ): Promise<{ runs: WorkflowToolRun[] }>;
}

// ---------------------------------------------------------------------------
// Helper: build a standard INVALID_INPUT error result
// ---------------------------------------------------------------------------

function missingParam(name: string): ToolResult {
  return {
    ok: false,
    summary: `Missing required parameter: "${name}"`,
    error: {
      code: "INVALID_INPUT",
      retryable: false,
      message: `The '${name}' parameter is required.`,
    },
  };
}

function notWired(action: string): ToolResult {
  return {
    ok: false,
    summary: `Workflow tool is not wired: ${action}`,
    error: {
      code: "ACTION_NOT_WIRED",
      retryable: false,
      message:
        "The host application must inject a WorkflowToolController before workflow tools can run.",
    },
  };
}

function executionError(action: string, error: unknown): ToolResult {
  const message = error instanceof Error ? error.message : String(error);
  return {
    ok: false,
    summary: `Workflow tool failed: ${action}`,
    error: {
      code: "WORKFLOW_TOOL_ERROR",
      retryable: false,
      message,
    },
  };
}

function optionalString(value: unknown): string | undefined {
  const text = typeof value === "string" ? value.trim() : "";
  return text ? text : undefined;
}

function mode(value: unknown): "mock" | "real" | undefined {
  if (value === "mock" || value === "real") return value;
  return undefined;
}

function stringRecord(value: unknown): Record<string, string> | undefined {
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    return undefined;
  }
  const record: Record<string, string> = {};
  for (const [key, item] of Object.entries(value)) {
    if (typeof item === "string") record[key] = item;
  }
  return record;
}

